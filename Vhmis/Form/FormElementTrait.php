namespace Vhmis\Form;

trait FormElementTrait
{

    /**
     * Name of field.
     *
     * @var string
     */
    protected $name;

    /**
     * Set name.
     *
     * @param string $name
     *
     * @return Field
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * Get name.
     *
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }
}
